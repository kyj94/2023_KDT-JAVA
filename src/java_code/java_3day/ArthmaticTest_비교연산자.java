package java_code.java_3day;

public class ArthmaticTest_비교연산자 {

	/* 비교연산자는 조건식을 표현함으로,
	* if문, while문과 같은 분기문에서 사용됨
	*/

	public static void main(String[] args) {
		int x = 10;
		int y = 8;

		System.out.println("x > y = " + (x>y));  // true
		System.out.println("x < y = " + (x<y)); // false
		System.out.println("x == y = " + (x==y)); // false
		System.out.println("x != y" + (x!=y)); // true
	}

}
