public class ArthmaticTest_산술연산자 {

	public static void main(String[] args) {
		int x = 100;
		int y = 200;

		System.out.println("x + y = " + (x+y));
		System.out.println("x - y = " + (x-y));
		System.out.println("x * y = " + (x*y));
		System.out.println("x / y = " + (x/y));
		System.out.println("x % 3 = " + (x%3)); // 짝수, 홀수 여부
	}

}
