package java_code.java_4day;

public class ControlFlow_for문 {

	public static void main(String[] args) {
		System.out.println("1. 자바 배우기");
		System.out.println("2. 자바 배우기");
		System.out.println("3. 자바 배우기");
		System.out.println("4. 자바 배우기");
		System.out.println("5. 자바 배우기");
		System.out.println();

		// "자바 배우기" 문자열 5번 출력
		for(int k=1;k<6;k++) {
			System.out.println(k+". 자바 배우기");
		}
	}

}
